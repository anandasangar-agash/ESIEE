DROP TABLE IF EXISTS joueur CASCADE;
DROP TABLE IF EXISTS equipe CASCADE;
DROP TABLE IF EXISTS match CASCADE;
DROP TABLE IF EXISTS participe CASCADE;

CREATE TABLE equipe (
  idE serial primary key,
  nomE varchar(25),
  pays varchar(25),
  capitaine int
);

CREATE TABLE joueur (
	numlicence int primary key,
	nom varchar(25),
	prenom varchar(25),
	naissance date,
	sexe char(1),
	idE int REFERENCES equipe(idE),
	role varchar(15)
);

CREATE TABLE match (
  idmatch serial primary key,
  date date,
  lieu varchar(20),
  vifdor int REFERENCES equipe(idE)
);

CREATE TABLE participe (
  idE int REFERENCES equipe(idE),
  idmatch int REFERENCES match(idmatch),
  score int,
  PRIMARY KEY (idE, idmatch)
);

CREATE VIEW gagnant AS (
  SELECT p1.idmatch, p1.idE
  FROM participe AS p1, participe AS p2
  WHERE p1.score > p2.score
  AND p1.idmatch = p2.idmatch
  UNION
  SELECT p1.idmatch, NULL
  FROM participe AS p1, participe AS p2
  WHERE p1.score = p2.score
  AND p1.idmatch = p2.idmatch
  AND p1.idE <> p2.idE
);

